package day02datatypesmethodcreation;

public class Variables01 {
    public static void main(String[] args) {

        //Create a variable for city name "Ankara" fist and then assign "Istanbul" for the String.

        String cityName = "Ankara";
        System.out.println(cityName);

        cityName = "Istanbul";
        System.out.println(cityName);

        //Create a plate code 34 change it into 35

        byte plateCode = 34;
        System.out.println(plateCode);

        plateCode = 35;
        System.out.println(plateCode);

        //Create a variable 'Is successful?' first assign false then true

        boolean isSuccessful = false;
        System.out.println(isSuccessful);

        isSuccessful = true;
        System.out.println(isSuccessful);
    }
}
